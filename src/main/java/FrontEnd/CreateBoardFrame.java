    private void initTable(){
        DefaultTableModel model = new DefaultTableModel(this.rows,this.columns);
        this.TablePreview.setModel(model);
    }
    
    private void createButtonsInPanel(Container pane) {
       this.temporalySections = new JButton[this.rows][this.columns];
        for (int i = 0; i < this.rows; i++) {
            for (int j = 0; j < this.columns; j++) {
                CreateBoard temp = new CreateBoard(i,j);
                pane.add(temp.getTempButton());
                temp.setBoundsButtons(i,j);
                this.temporalySections[i][j] = temp.getTempButton();               
                this.temporalySections[i][j].addActionListener(this);
            }
        }
        this.addComponentListener(this);        
    }
    
    private void resizePanelTable(){
        this.jPanel3.setBounds((this.columns)*80,60, WIDTH, HEIGHT);
        this.jPanel3.setSize(new Dimension(((this.columns)*80), ((this.rows+1)*70)+35));
    }
    
    private void resizePanelBoard(){
        if (this.columns*70 > 363 || this.rows*80 > 222) {
            this.jPanel1.setSize(new Dimension(((this.columns)*80), ((this.rows+1)*70)+35));        
            this.setSize(new Dimension(((this.columns)*80)*2, ((this.rows+1)*70)+35));            
        }
        
    }
    
    private void showAllInTable(){        
        Section[][] allSections = this.board.getSectionsBoard();
        for (int i = 0; i < this.rows; i++) {
            for (int j = 0; j < this.columns; j++) {
                try{
                    addItemPanelTable(verifyName(allSections[i][j]),i,j);
                }catch(NullPointerException e){
                    
                }                
            }            
        }                
    }
    
    private String verifyName(Section section){
        String name = "";
        if (section instanceof BasicService) {
            name = "Servicio";
        }else if (section instanceof Jail) {
            name = "Carcel";
        }else if (section instanceof Neutro) {
            name = "Neutro";
        }else if (section instanceof Property) {
            name = "Propiedad";
        }else if (section instanceof Start) {
            name = "Inicio";
        }else if (section instanceof TakeOne) {
            name = "Toma una Tarjeta";
        }else if (section instanceof Trap) {
            name = "Trampa";
        }        
        return name;
    }
    

