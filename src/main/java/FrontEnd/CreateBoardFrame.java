    private void ButtonCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonCreateActionPerformed
        this.game.setTurnSectionsEmpty();
        boolean isReady = this.game.reviewMap();
        boolean boardCreated = false;
        if (isReady == true) {
            fileManager fileM = new fileManager();
            try {
                JFileChooser fileChooser = new JFileChooser();
                fileChooser.addChoosableFileFilter(new FileNameExtensionFilter("game.board", "board"));
                fileChooser.setAcceptAllFileFilterUsed(false);
                int seleccion = fileChooser.showSaveDialog(this);
                if (seleccion == APPROVE_OPTION) {
                    fileM.saveBoard(fileChooser.getSelectedFile().getPath()+".board", this.game);
                    boardCreated = true;
                }
            } catch (HeadlessException  e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Info", JOptionPane.ERROR_MESSAGE);
            }
            if (boardCreated) {
                JOptionPane.showMessageDialog(null, "Tablero Creado con Exito");
                this.dispose();
            }            
        }else{
            JOptionPane.showMessageDialog(null, "Error al crear Tablero");
        }
    }//GEN-LAST:event_ButtonCreateActionPerformed

