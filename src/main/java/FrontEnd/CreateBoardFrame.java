    void addItemPanelTable(String name,int row, int column) {
        TableColumn columna = this.TablePreview.getColumnModel().getColumn(column);  
        ChangeCellColor TableCellRenderer = new ChangeCellColor();
        TableCellRenderer.setColumn(column);
        TableCellRenderer.setRow(row);
        columna.setCellRenderer(TableCellRenderer);
        this.TablePreview.setValueAt(name, row, column);              
    }


    
